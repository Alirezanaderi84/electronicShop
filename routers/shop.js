const express = require('express')
const router = express.Router()
const isAuth=require('../middleware/is-auth')
const shopController=require('../controllers/shop_controller')
router.get('/',shopController.getIndex)
router.get('/products',shopController.getProducts)
router.get('/products/:productId',shopController.getProductDeatail)
router.post('/cart', isAuth,shopController.postCart)
router.get('/cart', isAuth,shopController.getCart)
router.post('/cartDelete', isAuth,shopController.postCartDeleteProducts)
router.get('/checkout',isAuth,shopController.getCheckOut)
router.get('/paymentRequest',isAuth,shopController.getPayment)
router.get('/checkPayment',isAuth,shopController.checkPayment)
router.post('/createOrder', isAuth,shopController.postOrder)
router.get('/orders', isAuth,shopController.getOrder)
router.post('/addquantity', isAuth,shopController.addQuantityCart)
router.get('/invoices/:orderId',isAuth,shopController.getInvoices)
module.exports = router